    private void modifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifyActionPerformed

        if (this.OIcode.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter CURRENT ICODE");
        } else {
            try {
                Item i = new Item(this.Nicode.getText(), this.Ncmpny.getText(), this.Nprice.getText(),
                        this.Nqnty.getText(), this.Nname.getText(), this.Catagory.getSelectedItem().toString(), "tmp");
                java.sql.PreparedStatement ps = EmpPnl.con.prepareStatement("UPDATE `items` SET `icode`=?,`cmpny`=?,`iprice`=?,`qty`=?,`iname`=?,`catagory`=?,`lhby`=?,`data`=? WHERE icode=?");
                ps.setString(1, this.Nicode.getText());
                ps.setString(2, this.Ncmpny.getText());
                ps.setDouble(3, Double.parseDouble(this.Nprice.getText()));
                ps.setDouble(4, Double.parseDouble(this.Nqnty.getText()));
                ps.setString(5, this.Nname.getText());
                ps.setString(6, this.Catagory.getSelectedItem().toString());
                ps.setString(7, EmpLogin.empid);
                ps.setString(8, new Gson().toJson(i));
                ps.setString(9, this.OIcode.getText());
                if (ps.executeUpdate() != 0) {
                    JOptionPane.showMessageDialog(this, "Item Updated..");
                } else {
                    JOptionPane.showMessageDialog(this, "Item not Updated..");
                }

            } catch (SQLException ex) {
                Logger.getLogger(Modifi.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        this.setVisible(false);
    }//GEN-LAST:event_modifyActionPerformed

