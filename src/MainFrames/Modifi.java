    static java.sql.ResultSet autoFill(String icode) {
        java.sql.ResultSet tmp = null;
        try {
            java.sql.PreparedStatement ps = EmpPnl.con.prepareStatement("select * from items where icode = ?");
            ps.setString(1, icode);
            if ((tmp = ps.executeQuery()) != null) {
                tmp.next();
            } else {
                tmp = null;
            }

        } catch (SQLException ex) {
            Logger.getLogger(Modifi.class.getName()).log(Level.SEVERE, null, ex);
        }
        return tmp;
    }

