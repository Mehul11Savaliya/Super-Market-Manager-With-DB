    private void OIcodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OIcodeActionPerformed
        try {
            java.sql.ResultSet rs;
            if ((rs = this.autoFill(OIcode.getText().toString())) != null) {
                this.Nicode.setText(rs.getString(1));
                this.Ncmpny.setText(rs.getString(2));
                this.Nname.setText(rs.getString(5));
                this.Nqnty.setText(Double.toString(rs.getDouble(4)));
                this.Nprice.setText(Double.toString(rs.getDouble(3)));
            } else {
                JOptionPane.showMessageDialog(this, "Item - NOT FOUND.");
                this.OIcode.setText(null);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Item - NOT FOUND.");
            Logger.getLogger(EmpPnl.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_OIcodeActionPerformed

